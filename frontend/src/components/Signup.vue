<template>
    <div class="signup">
        <v-container style="max-width: 900px">
            <v-layout row wrap align-center>
                <v-flex>
                    <v-card flat>
                        <v-card-title class=" flat dense dark">
                            <h4 class="font-weight-regular">INSCRIPTION</h4>
                        </v-card-title>
                        <v-card-text class="font-weight-light">
                            <v-form ref="signupForm" autocomplete="off">
                                <v-text-field
                                    label="nom"
                                    v-model="lastName"
                                    type="text"
                                    required
                                    :rules="nameRules"
                                    color="#005C68"
                                ></v-text-field>
                                <v-text-field
                                    label="prénom"
                                    v-model="firstName"
                                    type="text"
                                    required
                                    :rules="nameRules"
                                    color="#005C68"
                                ></v-text-field>
                                <v-text-field
                                    label="e-mail"
                                    v-model="email"
                                    type="email"
                                    required
                                    :rules="emailRules"
                                    color="#005C68"
                                ></v-text-field>
                                <v-text-field
                                    label="mot de passe"
                                    v-model="password"
                                    :append-icon="showPassword ? '$vuetify.icons.eyeon' : '$vuetify.icons.eyeoff'"
                                    @click:append="showPassword = !showPassword"
                                    :type="showPassword ? 'text' : 'password'"
                                    required
                                    :rules="passwordRules"
                                    color="#005C68"
                                ></v-text-field>
                            </v-form>
                            <v-alert dense text type="error" v-if="errorMessage !== null" class="my-2 ">
                                {{ errorMessage }}
                            </v-alert> 
                        </v-card-text>
                        <v-card-actions class="justify-center">
                            <v-btn rounded depressed large min-width="200" color="#FFD7D7" class="mx-1 grey--text text--darken-2 font-weight-bold"
                                @click.prevent="signup" :loading="btnLoading">
                                S'INSCRIRE
                            </v-btn>
                            <v-snackbar v-model="snackbar" :timeout="2000">
                                <span>Votre inscription a bien été prise en compte ! 😉</span>
                            </v-snackbar>     
                        </v-card-actions>
                    </v-card>
                </v-flex>
            </v-layout> 
        </v-container>
    </div>
</template>

<script>
import AuthenticationService from '@/services/AuthenticationService'

export default {
    data() {
        return {
            lastName: "",
            firstName: "",
            email: "",
            password: "",
            nameRules: [
                v => !!v || 'Ce champ est obligatoire.',
                v => /^[a-zA-ZàâäéèêëîïôöùûüÿçæœA-ZÀÂÄÉÈÊËÎÏÔÖÙÛÜŸÇÆ ,.'-]+$/u.test(v) || 'Ce champ peut contenir les caractères alphabétiques qui sont utilisés dans la langue française et [ . ] [ \' ] [ - ].'
            ],
            emailRules: [
                v => !!v || 'Ce champ est obligatoire.',
                v => /^\w+([.-]?\w+)*@\w+([.-]?\w+)*(\.\w{2,3})+$/.test(v) || 'Veuillez saisir une adresse e-mail valide.'
            ],
            passwordRules: [
                v => !!v || 'Ce champ est obligatoire.',
                v => /(^(?=.*\d)(?=.*[a-z])(?=.*[A-Z])(?=.*[\W]).{8,64}$)/.test(v) || 'Un mot de passe doit être d\'une longueur minimale de 8 caractères comprenant au moins un majuscule, un miniscule, un chiffre et un caractère spécial.'
            ],
            showPassword: false,
            btnLoading: false,
            snackbar: false,

            // error message 
            errorMessage: null,
        }
    },
    methods: {
        async signup() {
            // If sign-up form is valid
            if (this.$refs.signupForm.validate()) {
                // Start button loader 
                this.btnLoading = true;
                // Send the form inputs to API (axios-sequelize-mysql)           
                try {
                    const response = await AuthenticationService.signup({
                        lastName: this.lastName,
                        firstName: this.firstName,
                        email: this.email,
                        password: this.password
                    })
                    console.log(response.data)
                    
                    // Pop-up snackbar
                    this.snackbar = true;

                    // Update data
                    this.$store.dispatch("logIn", response.data.token);
                    this.$store.dispatch("setUser", response.data.user);
                    this.$store.dispatch("getUserById", response.data.user.id);

                    // Stop button loading
                    this.btnLoading = false;

                    // Redirect to the main page after 2.2 seconds
                    setTimeout(function() {
                        window.location.href= "./";
                    }, 1500)
                }
                // Catch authentication error 
                catch (error) {
                    // Stop button roader 
                    this.btnLoading = false;
                    // Display authentication error message
                    this.errorMessage = error.response.data.error;
                }
            }
            // If sign-up form is not valid
            else {
                this.errorMessage = 'Oops, votre saisie ne respecte pas le format du formulaire. Veuillez le renseigner 😅';
            }
        }
    }
}
</script>

